      /**
       * Returns font classes from database
       * 
       * @param  string $font_name
       * @return array
       */
      static function font_database_record( $font_name ) {
        $option_name = 'volcanno_' . $font_name . '_list';

        return get_option( $option_name );
      }

      /**
       * Return font classes
       * 
       * @param  string $font_name
       * @return array
       */
      static function get_font_classes( $font_name ) {

        $icons = Volcanno_Icon_Fonts::font_database_record( $font_name );

        if ( !empty( $icons ) ) {
            return $icons;
        } else {
            $func_name = $font_name . '_classes';

            $icons = call_user_func( $func_name );

            return $icons;
        }
      }

